public class Switch{
public static void main(String args[]){
int a=2;
switch(a){
case 1:
System.out.println("it is a postive number");
break;
case 2:
System.out.println("it is zero");
break;
case 3:
System.out.println("it is a negative number");
break;
default:
System.out.println("no such case");
}
}
}
